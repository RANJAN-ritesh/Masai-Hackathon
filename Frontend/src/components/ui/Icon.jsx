import React from 'react';
import * as LucideIcons from 'lucide-react';

const Icon = ({ 
  name, 
  size = 20, 
  className = "", 
  color = "currentColor",
  ...props 
}) => {
  const IconComponent = LucideIcons[name];
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }
  
  return (
    <IconComponent 
      size={size} 
      className={`text-current ${className}`}
      style={{ color }}
      {...props}
    />
  );
};

